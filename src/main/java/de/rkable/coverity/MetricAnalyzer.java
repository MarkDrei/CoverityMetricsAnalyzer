package de.rkable.coverity;

import java.io.IOException;
import java.util.ArrayList;
import java.util.Arrays;
import java.util.Collection;
import java.util.List;
import java.util.Set;

import de.rkable.coverity.analyze.CallgrindAnalyzer;
import de.rkable.coverity.analyze.MetricsAnalyzer;
import de.rkable.coverity.input.CommandLineParser;
import de.rkable.coverity.input.Configuration;
import de.rkable.coverity.input.MetricsFileReader;
import de.rkable.coverity.metrics.Directory;
import de.rkable.coverity.metrics.DirectoryHierarchyGenerator;
import de.rkable.coverity.metrics.File;
import de.rkable.coverity.metrics.FileListGenerator;
import de.rkable.coverity.metrics.Method;

public class MetricAnalyzer {
    
    static MetricsAnalyzer analyzer = new CallgrindAnalyzer();

    public static void main(String[] args) {
        Configuration config = new CommandLineParser(args).getResult();
        
        if (config.hasErrors()) {
            System.err.println("Analyzes a metrics file generated by coverity\n"
                    + "Your parameters are invalid. Need -i and file name.\n");
            return;
        }

        MetricsFileReader fileReader = new MetricsFileReader(config.getInputFile());
        try {
            fileReader.parseFile();
        } catch (IOException e) {
            System.err.println("Failed to parse the given file: " + e.getMessage());
            e.printStackTrace();
            return;
        }
        
        List<Method> metricEntities = fileReader.getMetricEntities();
        FileListGenerator metricsList = new FileListGenerator();
        Collection<File> files = metricsList.generateFileMetrics(metricEntities);
        
        // filter for unwanted files
        Set<String> excludePatterns = config.getExcludePatterns();
        Collection<File> wantedFiles = new ArrayList<>();
        for (File file : files) {
            boolean excludeFile = false;
            for (String exclude : excludePatterns) {
                if (file.getPath().contains(exclude)) {
                    excludeFile = true;
                    break;
                }
            }
            if (!excludeFile) {
                wantedFiles.add(file);
            }
        }
        
        DirectoryHierarchyGenerator hierarchyGenerator = new DirectoryHierarchyGenerator();
        Directory hierarchy = hierarchyGenerator.buildHierarchy(wantedFiles);
        
        analyzer.startAnalysis(Arrays.asList(hierarchy));
        System.out.println(analyzer.getAnalysis());
    }

}
